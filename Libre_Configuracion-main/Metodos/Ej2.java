package Metodos;

public class Ej2 {
    public static void main(String[] args) {
        // Prueba de abs()
        System.out.println("abs(23.7) = " + Math.abs(23.7)); // 23.7
        System.out.println("abs(0.0) = " + Math.abs(0.0));   // 0.0
        System.out.println("abs(-23.7) = " + Math.abs(-23.7)); // 23.7

        // Prueba de ceil()
        System.out.println("ceil(9.2) = " + Math.ceil(9.2));   // 10.0
        System.out.println("ceil(-9.8) = " + Math.ceil(-9.8)); // -9.0

        // Prueba de cos()
        System.out.println("cos(0.0) = " + Math.cos(0.0)); // 1.0

        // Prueba de exp()
        System.out.println("exp(1.0) = " + Math.exp(1.0)); // 2.718281828459045
        System.out.println("exp(2.0) = " + Math.exp(2.0)); // 7.38905609893065

        // Prueba de floor()
        System.out.println("floor(9.2) = " + Math.floor(9.2));   // 9.0
        System.out.println("floor(-9.8) = " + Math.floor(-9.8)); // -10.0

        // Prueba de log()
        System.out.println("log(Math.E) = " + Math.log(Math.E)); // 1.0
        System.out.println("log(2.0) = " + Math.log(2.0)); // 0.6931471805599453

        // Prueba de max()
        System.out.println("max(2.3, 12.7) = " + Math.max(2.3, 12.7)); // 12.7
        System.out.println("max(-2.3, -12.7) = " + Math.max(-2.3, -12.7)); // -2.3

        // Prueba de min()
        System.out.println("min(2.3, 12.7) = " + Math.min(2.3, 12.7)); // 2.3
        System.out.println("min(-2.3, -12.7) = " + Math.min(-2.3, -12.7)); // -12.7

        // Prueba de pow()
        System.out.println("pow(2.0, 7.0) = " + Math.pow(2.0, 7.0)); // 128.0

        // Prueba de sin()
        System.out.println("sin(0.0) = " + Math.sin(0.0)); // 0.0

        // Prueba de sqrt()
        System.out.println("sqrt(900.0) = " + Math.sqrt(900.0)); // 30.0

        // Prueba de tan()
        System.out.println("tan(0.0) = " + Math.tan(0.0)); // 0.0
    }
}

